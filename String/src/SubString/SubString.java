package SubString;

public class SubString {

	public static void main(String[] args) {
		String s1="Swapnil";
		System.out.println(s1.substring(2,4));//2nd index pass krne pr index-1 show hota hai like humne 4 index pass kiya hai to 3rd index ka character show hoga.
		System.out.println(s1.substring(3));
		
		//Hum jo index pass Karte Hai vaha se aage ke character show krta hai.
	}

}
